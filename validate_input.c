int	validate_input(int argc, char **argv)
{
	int i;
	int j;

	if (argc != 10)
		return (0);
	i = 1;
	while (i <= 9)
	{
		j = 0;
		while (j < 9)
		{
			if (argv[i][j] == '\0'
				|| !((argv[i][j] <= '9' && argv[i][j] >= '1')
					|| argv[i][j] == '.'))
				return (0);
			j++;
		}
		if (argv[i][j] != '\0')
			return (0);
		i++;
	}
	return (1);
}
